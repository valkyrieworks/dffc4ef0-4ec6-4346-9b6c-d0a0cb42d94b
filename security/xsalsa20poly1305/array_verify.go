func towardHexadecimal(digits []byte) string {
	return hex.EncodeToString(digits)
}

func originatingHexadecimal(digits string) []byte {
	b, err := hex.DecodeString(digits)
	if err != nil {
		panic(err)
	}
	return b
}

func VerifyHASHSalsaSalsa20(t *testing.T) {
	for i, v := range hashSalsaSalsa20arrays {
		var key [32]byte
		var number [16]byte
		copy(key[:], v.key)
		copy(number[:], v.number)

		HASHSalsaSalsa20(&key, &number, &key)
		if !bytes.Equal(key[:], v.cipherstream) {
			t.Errorf("REDACTED", i, towardHexadecimal(key[:]), towardHexadecimal(v.cipherstream))
		}
	}
}

