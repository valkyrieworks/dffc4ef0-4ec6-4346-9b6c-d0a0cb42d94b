func VerifyArrays(t *testing.T) {
	for i, v := range arrays {
		if len(v.cleartext) == 0 {
			v.cleartext = make([]byte, len(v.sealedtext))
		}

		var number [24]byte
		copy(number[:], v.number)

		cipher, err := New(v.key)
		if err != nil {
			t.Error(err)
		}

		dst := cipher.Seal(nil, number[:], v.cleartext, v.ad)
		if !bytes.Equal(dst, v.sealedtext) {
			t.Errorf("REDACTED", i, towardHexadecimal(dst), towardHexadecimal(v.sealedtext))
		}
		unlock, err := cipher.Open(nil, number[:], dst, v.ad)
		if err != nil {
			t.Error(err)
		}
		if !bytes.Equal(unlock, v.cleartext) {
			t.Errorf("REDACTED", i, string(unlock), string(v.cleartext))
		}
	}
}

