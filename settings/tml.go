const FallbackPathMode = 0o700

var settingsBlueprint *template.Template

func initialize() {
	var err error
	layout := template.New("REDACTED").Funcs(template.FuncMap{
		"REDACTED": strings.Join,
	})
	if settingsBlueprint, err = layout.Parse(fallbackSettingsBlueprint); err != nil {
		panic(err)
	}
}

