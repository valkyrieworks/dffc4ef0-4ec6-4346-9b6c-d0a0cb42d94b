func AssureOrigin(originPath string) {
	if err := strongos.AssurePath(originPath, FallbackPathMode); err != nil {
		panic(err.Error())
	}
	if err := strongos.AssurePath(filepath.Join(originPath, FallbackSettingsPath), FallbackPathMode); err != nil {
		panic(err.Error())
	}
	if err := strongos.AssurePath(filepath.Join(originPath, FallbackDataPath), FallbackPathMode); err != nil {
		panic(err.Error())
	}

	settingsRecordRoute := filepath.Join(originPath, fallbackSettingsRecordRoute)

	//
	if !strongos.RecordPresent(settingsRecordRoute) {
		recordFallbackSettingsRecord(settingsRecordRoute)
	}
}

//
//
func recordFallbackSettingsRecord(settingsRecordRoute string) {
	PersistSettingsRecord(settingsRecordRoute, FallbackSettings())
}

//
func PersistSettingsRecord(settingsRecordRoute string, settings *Settings) {
	var reserve bytes.Buffer

	if err := settingsBlueprint.Execute(&reserve, settings); err != nil {
		panic(err)
	}

	strongos.ShouldRecordRecord(settingsRecordRoute, reserve.Bytes(), 0o644)
}

