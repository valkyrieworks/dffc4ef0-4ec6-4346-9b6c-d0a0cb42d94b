func AssureOrigin(originFolder string) {
	if err := cometos.AssureFolder(originFolder, StandardFolderMode); err != nil {
		panic(err.Error())
	}
	if err := cometos.AssureFolder(filepath.Join(originFolder, StandardSettingsFolder), StandardFolderMode); err != nil {
		panic(err.Error())
	}
	if err := cometos.AssureFolder(filepath.Join(originFolder, StandardDataFolder), StandardFolderMode); err != nil {
		panic(err.Error())
	}

	settingsEntryRoute := filepath.Join(originFolder, standardSettingsEntryRoute)

	//
	if !cometos.EntryPresent(settingsEntryRoute) {
		recordStandardSettingsEntry(settingsEntryRoute)
	}
}

//
//
func recordStandardSettingsEntry(settingsEntryRoute string) {
	RecordSettingsEntry(settingsEntryRoute, StandardSettings())
}

//
func RecordSettingsEntry(settingsEntryRoute string, settings *Settings) {
	var buffer bytes.Buffer

	if err := settingsPrototype.Execute(&buffer, settings); err != nil {
		panic(err)
	}

	cometos.ShouldRecordEntry(settingsEntryRoute, buffer.Bytes(), 0o644)
}

